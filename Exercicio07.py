#Declarar
Comprimento: float = 0
Largura: float = 0
Altura: float = 0
Volume: float = 0
#Incio
Comprimento = float(input('Coloque o comprimento do paralelepípedo:'))
Largura = float(input('Coloque a largura do paralelepípedo:'))
Altura = float(input('Coloque a altura do paralelepípedo:'))
Volume = (Comprimento*Largura*Altura)
print ('Aqui está o volume do paralelepípedo:', Volume)
